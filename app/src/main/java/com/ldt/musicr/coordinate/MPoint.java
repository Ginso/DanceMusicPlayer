package com.ldt.musicr.coordinate;

public class MPoint {
    public double Y;
    public double X;

    public MPoint(double x,double y) {
        X = x;
        Y = y;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }

}
